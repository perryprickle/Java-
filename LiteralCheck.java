class LiteralCheck
{		
	public static void main(String args[])
	{	
		int x=12;
		int y=010;
		int v=0xA;	
		System.out.println("integer literals");
		System.out.println("Decimal literals");
		System.out.println("4:"+x);
		System.out.println("Octa literals");
		System.out.println("010:"+y);
		System.out.println("Hexa literals");
		System.out.println("0xA:"+v);
		float f=12.34f;
		System.out.println("f:"+f);
		double b=45.4f;
		System.out.println("b:"+b);
		

		
		
	}
}